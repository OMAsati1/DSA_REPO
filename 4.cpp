int main()
{
    int n = 5;
    int i = 1;
    // cin >> n;
    while (i <= n)
    {
            // int spc=n-i;
            int k =1;
        while (k<=n-i+1)
        {
            cout <<k;
            // spc--;
            k++;
        }
    int j = 1;
        while (j <= 2*i-2)
        {
            cout << "*";
            j++;
        }
    int l = 1;
        while (l <= n-i+1)
        {
            cout << n-i-l+2;
            l++;
        }
        cout << endl; 
        i++;
    }
    return 0;
}
